    public void populateTable(){
        int colCount; 
        try{
            Connection conn = MySQLConnect.getConnection();
            Statement st = conn.createStatement();
            String query = "SELECT * FROM users";
            ResultSet rs = st.executeQuery(query);
            ResultSetMetaData rsData = rs.getMetaData();
            colCount = rsData.getColumnCount();
            
            DefaultTableModel tblModel = (DefaultTableModel)tblUser.getModel();
            tblModel.setRowCount(0);
            while(rs.next()){
                Vector colData = new Vector();
                for(int i = 1; i <= 4; i++){
                    colData.add(rs.getInt("UserID"));
                    colData.add(rs.getString("Username"));
                    colData.add(rs.getString("Userpassword"));
                    colData.add(rs.getString("Userlevel"));
                }
                tblModel.addRow(colData);
            }
        }catch(SQLException e){
            JOptionPane.showMessageDialog(null, e);
        }
    }    
