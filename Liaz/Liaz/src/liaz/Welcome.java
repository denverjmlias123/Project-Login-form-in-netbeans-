    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(check.equals("Add")){
            try{
                Connection conn = MySQLConnect.getConnection();
                PreparedStatement pst;
                String query = "INSERT INTO users (Username, Userpassword, Userlevel)VALUES(?, ?, ?)";
                pst = conn.prepareStatement(query);
                pst.setString(1, txtUser.getText());
                pst.setString(2, txtPass.getText());
                pst.setString(3, cmbUserlevel.getSelectedItem().toString());
                pst.executeUpdate();
                setDefault();
                populateTable();
            }catch(SQLException e){
                    JOptionPane.showMessageDialog(null, e);
            }
        }else if(check.equals("Update")){
                try{
                Connection conn = MySQLConnect.getConnection();
                PreparedStatement pst;
                String query = "UPDATE users SET Username = ?, Userpassword = ?, Userlevel = ? WHERE UserID = ?";
                pst = conn.prepareStatement(query);
                pst.setString(1, txtUser.getText());
                pst.setString(2, txtPass.getText());
                pst.setString(3, cmbUserlevel.getSelectedItem().toString());
                pst.setInt(4, UserID);
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "User sucessfully update");
                setDefault();
                populateTable();
            }catch(SQLException e){
                    JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_btnSaveActionPerformed

